;CONFIGURACION

DATO EQU 0x21

;INICIO DE PROGRAMA
	ORG	0X00
	GOTO	START

;CODIGO
START
	BSF	STATUS,5
	CLRF	TRISB 		; TODOS SON SALIDA
	BSF	TRISD, 7
	BCF	STATUS, RP0
	BCF	STATUS,5
	MOVLW	0X00
	MOVWF	PORTB
	GOTO	MENU

MENU
	BTFSC	PORTD, 7
	GOTO	DERECHA
	GOTO	IZQUIERDA
	GOTO	MENU

DERECHA
	CALL	MV1
	CALL	MV2
	;BCF	PORTD,7
	GOTO	MENU

IZQUIERDA
	CALL 	MV3
	CALL	MV4
	;BSF	PORTD,7
	GOTO	MENU

MV
	MOVF	DATO, W
	MOVWF	PORTB
	RETURN

MV1
	MOVLW	B'00000001'
	MOVWF	DATO
	CALL	MV
	RETURN

MV2
	MOVLW	B'00000010'
	MOVWF	DATO
	CALL	MV
	RETURN

MV3
	MOVLW	B'10000000'
	MOVWF	DATO
	CALL	MV
	RETURN

MV4
	MOVLW	B'01000000'
	MOVWF	DATO
	CALL	MV
	RETURN



END
